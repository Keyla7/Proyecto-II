    public void setUser(Users user){
        txtName.setText(user.getName());
        txtPassword.setText(user.getPassword());
        txtEmail.setText(user.getEmail());
        cbxProfile.setSelectedItem(user.getProfile());
    }
    public Users getUser(){
        String name = txtName.getText();
        String password = txtPassword.getText();
        String email = txtEmail.getText();
        String comboProfile = (String) cbxProfile.getSelectedItem();
        
        return new Users(name, password, email, comboProfile);
    }
    
    public void cargarCombo(String[] listaUsuarios) {
        String[] profile = {"Admin", "Employee"};
        this.cbxProfile.setModel(new DefaultComboBoxModel<>(profile));
    }
    
    public String devolverCombo() {
        return this.cbxProfile.getSelectedItem().toString();
    }
    
    public void limpiar(){
        txtEmail.setText("");
        txtName.setText("");
        txtPassword.setText("");
    }
    
    public void estadoTxt(boolean estado) { //metodo para ocultar la contrase√±a
        if (estado == true) {
            txtPassword.setEchoChar((char) 0);
        } else {
            txtPassword.setEchoChar('\u2022'); 
        }
    }
    
